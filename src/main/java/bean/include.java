package bean;

public class include {
	private String Gno;
    private String Ono;
    private String amount;
	public String getGno() {
		return Gno;
	}
	public void setGno(String gno) {
		Gno = gno;
	}
	public String getOno() {
		return Ono;
	}
	public void setOno(String ono) {
		Ono = ono;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
}
